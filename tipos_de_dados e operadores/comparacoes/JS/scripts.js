//comparações serve de parametros para comparaçoes 

//maior que >
console.log(10 > 5);
//menor que <
console.log(5 < 10);
//maior ou igual >=
console.log(10 >= 10);
//menor ou igual <=
console.log(10 <= 10);
//igual ==
console.log(10 == 10);
//diferente != 
console.log(20 != 5);
//identico ===
console.log(10 === 10.00);
//identico com tipo diferente !==
console.log(10 !== "10");